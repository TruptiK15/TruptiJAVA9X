package November.ex_20112024_Collection_List;

import java.util.*;

public class Task_ArrayList_Reverse {
    public static void main(String[] args) {

        int[] no = {1, 2, 3, 4, 5};
        System.out.print(" Reversed Array : ");
        for (int i = no.length - 1; i >= 0; i--) {
            System.out.print(no[i] + " ");
        }

    }
}
