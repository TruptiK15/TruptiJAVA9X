package October.ex_18102024_Switch;

public class Switch_Statement4 {
    public static void main(String[] args) {

        char code = 'C';
        switch(code){
            default:
                System.out.println("Helloooo......");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
