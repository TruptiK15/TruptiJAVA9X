package October.ex_30102024_Array2;

public class Array_For_each_String {
    public static void main(String[] args) {

        String[] name = {"trupti", "amey", "sneha", "kiara"};
        for (String i:name){
            System.out.println(i);
        }

        int[] num = {10, 5, 22, 66};
        for (int i:num){
            System.out.println(i);
        }
    }
}
