package October.ex_10102024;

public class Constants_example2 {

    public static void main(String[] args) {

        //constant - special variable whose value can't be modify / change during execution
        //final keyword

        final int a = 10;
        final String str = "Trupti";
        final double doub = 999.99;

        System.out.println(a);
        System.out.println(str);
        System.out.println(doub);
    }

}
