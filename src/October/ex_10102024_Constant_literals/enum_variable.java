package October.ex_10102024_Constant_literals;

public class enum_variable {
    public static void main(String[] args) {

        //int enum = 9;   //not possible - enum is keyword
        int Enum = 9;      //not a keyword
        System.out.println(Enum);

        //char char = 'A';    //not possible - char is keyword
        char c = 'A';
        System.out.println(c);

    }
}
