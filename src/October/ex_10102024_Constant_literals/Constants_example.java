package October.ex_10102024_Constant_literals;

public class Constants_example {

    public static void main(String[] args) {

        //constant - special variable whose value can't be modify / change during execution
        //final keyword

        final float pi = 3.14f;
        //pi = 3;  //value can be changed without constant variable
        System.out.println(pi);
    }

}
