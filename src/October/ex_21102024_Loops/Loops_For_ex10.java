package October.ex_21102024_Loops;

public class Loops_For_ex10 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            if (i == 5) {
                System.out.println("Five");
            } else {
                System.out.println(i);
            }
        }
    }
}
