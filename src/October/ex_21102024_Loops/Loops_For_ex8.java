package October.ex_21102024_Loops;
public class Loops_For_ex8 {
    public static void main(String[] args) {

        int i = 10;
        for (; i > 0; i--) {
            //as initialization is done before for it will execute
            //print reverse 10 to 1
            System.out.println(i);
        }
    }
}
